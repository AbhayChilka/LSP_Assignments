int Addition(int A, int B)
{
    int Ans = 0;

    Ans = A + B;

    return Ans;
}

int Subtraction(int A, int B)
{
    int Ans = 0;

    Ans = A - B;

    return Ans;
}

int Multiplication(int A, int B)
{
    int Ans = 0;

    Ans  = A * B;

    return Ans;
}

float Division(int A, int B)
{
    float Ans  = 0;

    Ans = A / B;

    return Ans;
}